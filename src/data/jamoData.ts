import type { JamoItem, Stroke } from '../types';

const line = (
  id: string,
  order: number,
  start: [number, number],
  end: [number, number],
  description: string,
): Stroke => ({
  id,
  order,
  label: `${order}획`,
  description,
  svgPath: `M ${start[0]} ${start[1]} L ${end[0]} ${end[1]}`,
  start,
  end,
});

const circle = (
  id: string,
  order: number,
  center: [number, number],
  radius: number,
  description: string,
): Stroke => ({
  id,
  order,
  label: `${order}획`,
  description,
  svgPath: [
    `M ${center[0]} ${center[1] - radius}`,
    `A ${radius} ${radius} 0 1 1 ${center[0]} ${center[1] + radius}`,
    `A ${radius} ${radius} 0 1 1 ${center[0]} ${center[1] - radius}`,
  ].join(' '),
  start: [center[0], center[1] - radius],
  end: [center[0], center[1] - radius],
});

const makeItem = (
  char: string,
  title: string,
  group: 'consonant' | 'vowel',
  learningNote: string,
  example: string,
  strokes: Stroke[],
): JamoItem => ({
  id: char,
  char,
  title,
  group,
  categoryLabel: group === 'consonant' ? '자음' : '모음',
  strokeCount: strokes.length,
  learningNote,
  example,
  strokes,
});

